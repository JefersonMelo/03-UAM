using System;

namespace Imprimir_Contador
{
    class Program
    {
        /*
        1. Faça um algoritmo que imprima a seguinte
        sequência de números de 1 a 100.
        */

        static void Main( string[] args )
        {
            for (int i = 1; i <= 100; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
