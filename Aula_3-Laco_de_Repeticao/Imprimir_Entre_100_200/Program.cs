using System;

namespace Imprimir_Entre_100_200
{
    class Program
    {
        /*
        2. Escreva um algoritmo que imprima todos os
        números inteiros de 100 a 200.
        */

        static void Main( string[] args )
        {
            for (int i = 100; i <= 200; i++)
            {
                Console.Write(i + ", ");
            }
        }
    }
}
